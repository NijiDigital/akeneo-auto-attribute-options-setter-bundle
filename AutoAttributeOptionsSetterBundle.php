<?php

namespace Niji\AutoAttributeOptionsSetterBundle;

use Akeneo\Tool\Bundle\StorageUtilsBundle\DependencyInjection\Compiler\ResolveDoctrineTargetRepositoryPass;
use Niji\AutoAttributeOptionsSetterBundle\DependencyInjection\Compiler\AttributeOptionCompilerPass;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\HttpKernel\Bundle\Bundle;

class AutoAttributeOptionsSetterBundle extends Bundle
{
    public function build(ContainerBuilder $container)
    {
        parent::build($container); // TODO: Change the autogenerated stub
        $container->addCompilerPass(new AttributeOptionCompilerPass());
        $container->addCompilerPass(new ResolveDoctrineTargetRepositoryPass('pim_niji_attribute_repository'));
    }
}
